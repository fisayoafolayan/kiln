func TestParse_FindsTables(t *testing.T) {
	dir := t.TempDir()
	writeModel(t, dir, "users.bob.go", usersModel)
	writeModel(t, dir, "posts.bob.go", postsModel)

	p := bob.New(dir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	if len(schema.Tables) != 2 {
		t.Errorf("got %d tables, want 2", len(schema.Tables))
	}
	if _, ok := schema.TableMap["users"]; !ok {
		t.Error("expected users table")
	}
	if _, ok := schema.TableMap["posts"]; !ok {
		t.Error("expected posts table")
	}
}

