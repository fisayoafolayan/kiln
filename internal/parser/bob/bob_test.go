// TestParse_RStructResolvesNonObviousFK verifies that the R struct
// correctly resolves FKs where the column name doesn't match the table name.
// e.g. articles.author_id → users (not "authors").
func TestParse_RStructResolvesNonObviousFK(t *testing.T) {
	// articles has author_id which points to users, not "authors".
	// Without R struct parsing, the _id heuristic would look for an "authors" table.
	const articlesModel = `// Code generated by BobGen psql v0.42.0. DO NOT EDIT.
package models

import "github.com/gofrs/uuid/v5"

type Article struct {
	ID       uuid.UUID ` + "`" + `db:"id,pk" ` + "`" + `
	AuthorID uuid.UUID ` + "`" + `db:"author_id" ` + "`" + `
	Title    string    ` + "`" + `db:"title" ` + "`" + `
	R articleR ` + "`" + `db:"-" ` + "`" + `
}

type ArticleSlice []*Article
type articleR struct {
	Author *User
}
`

	dir := t.TempDir()
	writeModel(t, dir, "users.bob.go", usersModel)
	writeModel(t, dir, "articles.bob.go", articlesModel)

	p := bob.New(dir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	articles := schema.TableMap["articles"]
	if articles == nil {
		t.Fatal("articles table not found")
	}

	if len(articles.ForeignKeys) == 0 {
		t.Fatal("articles should have a FK to users via author_id")
	}

	fk := articles.ForeignKeys[0]
	if fk.SourceColumn.Name != "author_id" {
		t.Errorf("FK source column = %q, want author_id", fk.SourceColumn.Name)
	}
	if fk.TargetTable.Name != "users" {
		t.Errorf("FK target table = %q, want users", fk.TargetTable.Name)
	}

	// Verify users has the reverse reference
	users := schema.TableMap["users"]
	if len(users.ReferencedBy) == 0 {
		t.Error("users should be referenced by articles")
	}
}
