func TestParse_ExtractsColumns(t *testing.T) {
	dir := t.TempDir()
	writeModel(t, dir, "users.bob.go", usersModel)

	p := bob.New(dir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	users := schema.TableMap["users"]
	if users == nil {
		t.Fatal("users table not found")
	}

	if len(users.Columns) != 7 {
		t.Errorf("got %d columns, want 7", len(users.Columns))
	}

	// Primary key
	if users.PrimaryKey == nil {
		t.Fatal("PrimaryKey is nil")
	}
	if users.PrimaryKey.Name != "id" {
		t.Errorf("PrimaryKey.Name = %q, want id", users.PrimaryKey.Name)
	}

	// Nullable field
	bio := users.ColumnMap["bio"]
	if bio == nil {
		t.Fatal("bio column not found")
	}
	if !bio.Nullable {
		t.Error("bio should be nullable")
	}

	// Non-nullable field
	email := users.ColumnMap["email"]
	if email == nil {
		t.Fatal("email column not found")
	}
	if email.Nullable {
		t.Error("email should not be nullable")
	}
}

func TestParse_ResolvesGoTypes(t *testing.T) {
	dir := t.TempDir()
	writeModel(t, dir, "users.bob.go", usersModel)

	p := bob.New(dir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	users := schema.TableMap["users"]

	cases := []struct {
		col          string
		wantTypeName string
		wantNullable bool
	}{
		{"id", "uuid.UUID", false},
		{"email", "string", false},
		{"bio", "string", true},
		{"created_at", "time.Time", false},
	}

	for _, c := range cases {
		col := users.ColumnMap[c.col]
		if col == nil {
			t.Errorf("column %q not found", c.col)
			continue
		}
		if col.GoType.Name != c.wantTypeName {
			t.Errorf("col %q GoType.Name = %q, want %q", c.col, col.GoType.Name, c.wantTypeName)
		}
		if col.Nullable != c.wantNullable {
			t.Errorf("col %q Nullable = %v, want %v", c.col, col.Nullable, c.wantNullable)
		}
	}
}

func TestParse_WiresForeignKeys(t *testing.T) {
	dir := t.TempDir()
	writeModel(t, dir, "users.bob.go", usersModel)
	writeModel(t, dir, "posts.bob.go", postsModel)

	p := bob.New(dir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	posts := schema.TableMap["posts"]
	if posts == nil {
		t.Fatal("posts table not found")
	}

	if len(posts.ForeignKeys) == 0 {
		t.Fatal("posts should have at least one FK")
	}

	fk := posts.ForeignKeys[0]
	if fk.TargetTable.Name != "users" {
		t.Errorf("FK target = %q, want users", fk.TargetTable.Name)
	}
	if fk.SourceColumn.Name != "user_id" {
		t.Errorf("FK source column = %q, want user_id", fk.SourceColumn.Name)
	}

	// users should have ReferencedBy
	users := schema.TableMap["users"]
	if len(users.ReferencedBy) == 0 {
		t.Error("users should be referenced by posts")
	}
}

func TestParse_ExtractsEnumsFromDBInfo(t *testing.T) {
	// Create models dir inside a parent so dbinfo can be a sibling.
	parent := t.TempDir()
	modelsDir := filepath.Join(parent, "models")
	dbinfoDir := filepath.Join(parent, "dbinfo")
	os.MkdirAll(modelsDir, 0755)
	os.MkdirAll(dbinfoDir, 0755)

	writeModel(t, modelsDir, "users.bob.go", usersModel)

	// Write a dbinfo file with CHECK constraints containing enum values.
	dbinfoContent := `// Code generated by BobGen psql v0.42.0. DO NOT EDIT.
package dbinfo

type userChecks struct{}

func (c userChecks) AsSlice() []check {
	return []check{
		{constraint: constraint{Name: "users_role_check", Columns: []string{"role"}}, Expression: "((role)::text = ANY ((ARRAY['member'::text, 'moderator'::text, 'admin'::text])::text[]))"},
	}
}
`
	os.WriteFile(filepath.Join(dbinfoDir, "users.bob.go"), []byte(dbinfoContent), 0644)

	// Also need the bob_types file for it to be a valid dbinfo dir.
	typesContent := `package dbinfo

type check struct {
	constraint
	Expression string
}
type constraint struct {
	Name    string
	Columns []string
}
`
	os.WriteFile(filepath.Join(dbinfoDir, "bob_types.bob.go"), []byte(typesContent), 0644)

	p := bob.New(modelsDir, ir.DriverPostgres)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	users := schema.TableMap["users"]
	if users == nil {
		t.Fatal("users table not found")
	}

	role := users.ColumnMap["role"]
	if role == nil {
		t.Fatal("role column not found")
	}
	if len(role.EnumValues) == 0 {
		t.Fatal("role.EnumValues should be populated from CHECK constraint")
	}
	want := []string{"member", "moderator", "admin"}
	if len(role.EnumValues) != len(want) {
		t.Fatalf("got %d enum values, want %d: %v", len(role.EnumValues), len(want), role.EnumValues)
	}
	for i, v := range want {
		if role.EnumValues[i] != v {
			t.Errorf("enum value [%d] = %q, want %q", i, role.EnumValues[i], v)
		}
	}
}

func TestParse_EnumConfigOverridesAutoDetection(t *testing.T) {
	// Same setup as above but we'll apply config override in the generator.
	// Here just verify auto-detection works — the generator test covers override.
	parent := t.TempDir()
	modelsDir := filepath.Join(parent, "models")
	dbinfoDir := filepath.Join(parent, "dbinfo")
	os.MkdirAll(modelsDir, 0755)
	os.MkdirAll(dbinfoDir, 0755)

	writeModel(t, modelsDir, "users.bob.go", usersModel)

	// Simple MySQL-style CHECK
	dbinfoContent := `package dbinfo

type userChecks struct{}

func (c userChecks) AsSlice() []check {
	return []check{
		{constraint: constraint{Name: "users_role_check", Columns: []string{"role"}}, Expression: "(role IN ('member', 'admin'))"},
	}
}
`
	os.WriteFile(filepath.Join(dbinfoDir, "users.bob.go"), []byte(dbinfoContent), 0644)
	os.WriteFile(filepath.Join(dbinfoDir, "bob_types.bob.go"), []byte("package dbinfo\n"), 0644)

	p := bob.New(modelsDir, ir.DriverMySQL)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	role := schema.TableMap["users"].ColumnMap["role"]
	if len(role.EnumValues) != 2 {
		t.Fatalf("got %d enum values, want 2: %v", len(role.EnumValues), role.EnumValues)
	}
	if role.EnumValues[0] != "member" || role.EnumValues[1] != "admin" {
		t.Errorf("unexpected enum values: %v", role.EnumValues)
	}
}

func TestParse_ExtractsMaxLengthFromDBInfo(t *testing.T) {
	parent := t.TempDir()
	modelsDir := filepath.Join(parent, "models")
	dbinfoDir := filepath.Join(parent, "dbinfo")
	os.MkdirAll(modelsDir, 0755)
	os.MkdirAll(dbinfoDir, 0755)

	writeModel(t, modelsDir, "users.bob.go", usersModel)

	// Write a dbinfo file with varchar column types.
	dbinfoContent := `package dbinfo

var Users = Table{
	Name: "users",
	Columns: userColumns{
		Email: column{Name: "email", DBType: "varchar(255)"},
		Name:  column{Name: "name", DBType: "varchar(100)"},
		Role:  column{Name: "role", DBType: "varchar(50)"},
		Bio:   column{Name: "bio", DBType: "text"},
	},
}

type Table struct {
	Name    string
	Columns userColumns
}

type userColumns struct {
	Email column
	Name  column
	Role  column
	Bio   column
}

type column struct {
	Name   string
	DBType string
}
`
	os.WriteFile(filepath.Join(dbinfoDir, "users.bob.go"), []byte(dbinfoContent), 0644)
	os.WriteFile(filepath.Join(dbinfoDir, "bob_types.bob.go"), []byte("package dbinfo\n"), 0644)

	p := bob.New(modelsDir, ir.DriverMySQL)
	schema, err := p.Parse()
	if err != nil {
		t.Fatalf("Parse() error: %v", err)
	}

	users := schema.TableMap["users"]
	if users == nil {
		t.Fatal("users table not found")
	}

	cases := []struct {
		col     string
		wantMax *int
	}{
		{"email", intPtr(255)},
		{"name", intPtr(100)},
		{"role", intPtr(50)},
		{"bio", nil}, // text has no max length
	}

	for _, c := range cases {
		col := users.ColumnMap[c.col]
		if col == nil {
			t.Errorf("column %q not found", c.col)
			continue
		}
		if c.wantMax == nil {
			if col.MaxLength != nil {
				t.Errorf("col %q MaxLength = %d, want nil", c.col, *col.MaxLength)
			}
		} else {
			if col.MaxLength == nil {
				t.Errorf("col %q MaxLength = nil, want %d", c.col, *c.wantMax)
			} else if *col.MaxLength != *c.wantMax {
				t.Errorf("col %q MaxLength = %d, want %d", c.col, *col.MaxLength, *c.wantMax)
			}
		}
	}
}

